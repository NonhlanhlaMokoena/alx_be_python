#Simple Interest Calculator. This activity is about calculating the simple interest where the principal amount(initial investment) is $1000, rate of interest is 0.05(representing 5% annual interest rate) and the time is 3 years.

#Step 1: Declare variables and their values
principal = 1000
rate = 0.05
time = 3 

#Step 2: Use this simple interest formula
simple_interest = principal * rate * time

#Step 3: Print the calculated interest is a sentence
print("The simple interest is:", "", simple_interest) 
